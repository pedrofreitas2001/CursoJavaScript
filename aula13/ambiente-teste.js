/* Exemplo estrutura de repetição while (enquanto) */

var c = 1
while (c <= 6) {
    console.log(`Passo ${c}.`)
    c++
}

// ------------------------------------------------ //

/* Exemplo estrutura de repetição do...while (faça...enquanto) */

var c = 1
do {
    console.log(`Passo ${c}.`)
    c++
} while ( c <= 6 )